class Token(BaseModel):
    access_token: str
    token_type: str


class TokenData(BaseModel):
    username: str | None = None


class User(BaseModel):
    username: str
    email: str | None = None
    full_name: str | None = None
    disabled: bool | None = None

class UserRegister(BaseModel):
    username: str
    email: str | None = None
    password: str


class UserInDB(User):
    hashed_password: str


class UserDB(User):
    id: int
